package duncan;
public class StaircasePrinter {
    public static void main(String[] args) {

        for (int i = 0; i < 2; i++)
        {
        System.out.println("            +---+");
        System.out.println("            |   |");
        System.out.println("        +---+---+");
        System.out.println("        |   |   |");
        System.out.println("    +---+---+---+");
        System.out.println("    |   |   |   |");
        System.out.println("+---+---+---+---+");
        System.out.println("|   |   |   |   |");
        System.out.println("+---+---+---+---+");
        System.out.println("\n");
        }
    }
}
